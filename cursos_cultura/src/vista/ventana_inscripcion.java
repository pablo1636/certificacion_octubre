    private void tematicasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tematicasItemStateChanged
        // TODO add your handling code here:
        //cambio estado combobox
        if(evt.getStateChange() == ItemEvent.SELECTED)
       {
           if(this.tematicas.getSelectedIndex()>0)
           {
               
            String idString = this.tematicas.getSelectedItem().toString();
            String parte[]= idString.split(" ");
            int id = Integer.parseInt(parte[0]);
            this.tematicas.getSelectedItem().toString();
            limpiarTabla();
            LoadCursos(id);
               
           }
       }
        
        
        
    }//GEN-LAST:event_tematicasItemStateChanged

