    public void llenarTematicas(){
        
        listaTematica = oper.tematicaVer();
        tematicas.removeAllItems();
        tematicas.addItem("Seleccione Tematica");
        for (Tematica tem : listaTematica) {
                tematicas.addItem(""+ tem.getId()+" "+ tem.getNombre() + "");
        }
        
        }
    
    
    
    void limpiarTabla(){
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(0);
        while(jTable1.getRowCount()!=0){
            ((DefaultTableModel)jTable1.getModel()).removeRow(0);
        }
    }
    public void LoadCursos(int id){
        List<Curso> cursos = oper.cursosVer(id);
        if(cursos.size()>0){
            Iterator consulta = cursos.iterator();
            
            while(consulta.hasNext()){
                DefaultTableModel tabla = (DefaultTableModel)jTable1.getModel();
                Vector datos = new Vector();
                Curso fila = (Curso)consulta.next();
                datos.add(fila.getId());
                datos.add(fila.getNombre());
                datos.add(fila.getProfesor());
                datos.add(fila.getAula());
                datos.add(fila.getDuracion());
                datos.add(fila.getCargaHoraria());
                datos.add(fila.getCupo());
                tabla.addRow(datos);
                
            }
        }else{
            JOptionPane.showMessageDialog(null, "No hay registros");
        }
    }
